package maths;
public class calc{
	public float sum(float a,float b){
		return a+b;
	}
	public float sub(float a,float b){
		return a-b;
	}
	public float mul(float a,float b){
		return a*b;
	}
	public float div(float a,float b){
		return a/b;
	}
	public float mod(float a,float b){
		return a%b;
	}
	
}